#include <iostream>
#include <string>

int main() {
    int a = 17;
    int b = 5;

    std::cout << "Penambahan: " << a + b << std::endl;
    std::cout << "Pengurangan: " << a - b << std::endl;
    std::cout << "Perkalian: " << a * b << std::endl;
    std::cout << "Pembagian: " << a / b << std::endl;

    float c = 17.0f;
    float d = 5.0f;

    std::cout << "Pembagian float: " << c / d << std::endl;

    return 0;
}

